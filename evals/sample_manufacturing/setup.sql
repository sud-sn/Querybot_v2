ATTACH DATABASE ':memory:' AS MFG;

CREATE TABLE MFG.PRODUCTS (
    PRODUCT_ID INTEGER PRIMARY KEY,
    PRODUCT_NAME VARCHAR NOT NULL,
    UNIT_PRICE DECIMAL(12, 2) NOT NULL
);

CREATE TABLE MFG.PRODUCTION_RUNS (
    RUN_ID INTEGER PRIMARY KEY,
    PRODUCT_ID INTEGER NOT NULL,
    RUN_DATE DATE NOT NULL,
    PLANNED_UNITS INTEGER NOT NULL,
    GOOD_UNITS INTEGER NOT NULL,
    SCRAP_UNITS INTEGER NOT NULL,
    DIRECT_MATERIAL_COST DECIMAL(12, 2) NOT NULL,
    LABOR_COST DECIMAL(12, 2) NOT NULL,
    DOWNTIME_MINUTES INTEGER NOT NULL
);

CREATE TABLE MFG.SALES (
    ORDER_ID INTEGER PRIMARY KEY,
    PRODUCT_ID INTEGER NOT NULL,
    SALE_DATE DATE NOT NULL,
    QUANTITY INTEGER NOT NULL,
    REVENUE DECIMAL(12, 2) NOT NULL
);

